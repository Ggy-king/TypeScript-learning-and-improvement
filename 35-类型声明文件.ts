// TS中的两种文件类型
/**
 * .ts文件  -- implementation(代码实现文件)
 * 1 既包含类型信息又可执行代码
 * 2 可以被编译成.js文件 执行代码
 * 3 用途 编写程序代码的地方
 */

/**
 * .d.ts文件  -- declaration(类型声明文件)
 * 1 只包含类型信息的类型声明文件 不包括可执行代码
 * 2 不会生成.js文件 仅仅用于提供类型信息
 * 3 用途 为js提供类型信息
 */